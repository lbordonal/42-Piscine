void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	x;
	int	y;

	while (size > 0)
	{
		i = 0;
		while (i <= (size - 1))
		{
			x = tab[i];
			y = tab[(i + 1)];
			if (x > y)
			{
				tab[i] = y;
				tab[(i + 1)] = x;
			}
			i++;
		}
		size--;
	}
}
